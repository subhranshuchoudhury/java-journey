// Math Operators P-II

class HelloWorld {
    public static void main(String[] args) {
        
        int num = 10;
        int num2 = 3;
       
       // %
        
        System.out.println(num%num2); // op - 1 how? (3x3+1)
        
        // ++
        
        num++;
        System.out.println(num); // op - 11
        
        // --
        
        num--;
        System.out.println(num); // op - 10
        
        // +=
        
        num2 += num;
        System.out.println(num2); // op - 13
        
        // -=
        num2 -= num2;
        System.out.println(num2); // op - 0
    }
}

