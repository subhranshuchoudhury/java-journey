import java.util.Scanner;
public class Main
{
	public static void main(String[] args) {
	    
	    System.out.println("1 to 19 Even or Odd: ");
	    
	    for (int i=0;i<20;i++){
	        if(i%2==0){
	            System.out.println(i + " -- " + "even");
	        }else{
	            System.out.println(i + " -- " + "odd");
	        }
	    } 
	}
}
